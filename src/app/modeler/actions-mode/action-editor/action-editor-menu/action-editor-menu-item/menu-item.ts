export class MenuItem {
  constructor(id: string, title: string) {
    this._id = id;
    this._title = title;
  }

  private _id: string;

  get id(): string {
    return this._id;
  }

  set id(value: string) {
    this._id = value;
  }

  private _title: string;

  get title(): string {
    return this._title;
  }

  set title(value: string) {
    this._title = value;
  }
}
